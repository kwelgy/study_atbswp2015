    random.shuffle(keys)
    questions = ['What is the capital of {0}?'.format(state) for state in keys]
    answers = [capitals[state] for state in keys]
    write_file('Quiz ' + str(i) + '.txt', '\n'.join(questions))
    write_file('Answers ' + str(i) + '.txt', '\n'.join(answers))